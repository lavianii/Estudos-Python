tipo = input('Digite algo: ')
print(type(tipo))
print('e alfabetico', tipo.isalpha())
print('alpha numerico? ', tipo.isalnum())
print('letra minuscula? ', tipo.islower())
print('e um numero? ', tipo.isnumeric())
print('ela é um espaço? ', tipo.isspace())
print('numero decimal? ', tipo.isdecimal())
print('letra maiscula? ', tipo.isupper())


